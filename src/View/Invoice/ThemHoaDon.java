    final void hienThiDuLieuKhachHangCombo() {
        ResultSet result = null;
        try {
            // Truy vấn dữ liệu từ cơ sở dữ liệu
            result = iv.hienThiHoaDonKhachHang();

            // Xóa tất cả các mục cũ trong ComboBox trước khi thêm mới
            cbbKhachHang.removeAllItems();

            // Thêm tên khách hàng vào ComboBox
            while (result.next()) {
                cbbKhachHang.addItem(result.getString("TenKhachHang"));
            }
        } catch (SQLException e) {
            // Xử lý lỗi và in thông báo lỗi
            JOptionPane.showMessageDialog(this, "Lỗi khi truy xuất dữ liệu khách hàng: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

