    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String soHoaDon = txtSoHoaDon.getText(); // Lấy số hóa đơn từ trường nhập liệu
            String tenKhachHang = cbbKhachHang.getSelectedItem().toString(); // Lấy tên khách hàng từ ComboBox
            int khachHangId = iv.getKhachHangIdByName(tenKhachHang); // Lấy KhachHangID từ tên khách hàng

            // Chuyển đổi TongTien từ String sang BigDecimal và kiểm tra giá trị hợp lệ
            BigDecimal tongTien = null;
            try {
                tongTien = new BigDecimal(txtTongTien.getText()); // Chuyển đổi giá trị sang BigDecimal
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Số tiền không hợp lệ", "Thông báo", JOptionPane.ERROR_MESSAGE);
                return; // Dừng phương thức nếu nhập liệu không hợp lệ
            }

//             Lấy giá trị Thuế Tiền (nếu cần) và trạng thái
             BigDecimal thueTien = new BigDecimal(txtThueTien.getText()); // Bỏ qua thuế tiền nếu không cần
            String trangThai = cbbTrangThai.getSelectedItem().toString(); // Lấy trạng thái từ ComboBox

            // Lấy ngày lập và ngày hạn thanh toán
            Date ngayLapHoaDon = (Date) NgayLapHoaDon.getDate();
            Date ngayHanThanhToan = (Date) NgayHanThanhToan.getDate();

            // Kiểm tra thông tin nhập vào
            if (ngayLapHoaDon == null || ngayHanThanhToan == null || khachHangId == -1) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Tạo đối tượng HoaDon và thiết lập các thuộc tính
            HoaDon obj = new HoaDon();
            obj.setSoHoaDon(soHoaDon);
            
            KhachHang khachHang = new KhachHang();
            khachHang.setKhachHangId(khachHangId);
            obj.setKhachHang(khachHang); // Sử dụng KhachHangID
            obj.setTongTien(tongTien); // Thiết lập tổng tiền
            obj.setThueTien(thueTien); // Nếu cần có thể thiết lập thuế tiền
            obj.setTrangThai(trangThai);
            obj.setNgayLapHoaDon(ngayLapHoaDon);
            obj.setNgayHanThanhToan(ngayHanThanhToan);

            // Thêm hóa đơn vào cơ sở dữ liệu
            iv.themHoaDon(obj);

            // Cập nhật dữ liệu trong form (nếu form không phải null)
            if (this.form != null) {
                this.form.refreshData(); // Cập nhật lại dữ liệu trong bảng
            }

            // Thông báo thêm hóa đơn thành công và đóng cửa sổ
            JOptionPane.showMessageDialog(this, "Thêm hóa đơn thành công");
            dispose(); // Đóng cửa sổ ThemHoaDon

        } catch (SQLException ex) {
            // Xử lý lỗi SQL
            JOptionPane.showMessageDialog(this, "Lỗi khi thêm hóa đơn: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ThemHoaDon.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            // Xử lý lỗi chung
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

